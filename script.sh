function banner() {
    local index=$((RANDOM % ${#banners[@]}))
    echo -e "\e[32m${banners[$index]}\e[0m"
}

function menu() {
    echo "Select an option:"
    echo "1. PVUSD Remote"
    echo "2. PVUSD Assisted"
    echo "3. "
    echo "4. "
    echo "5. "
    echo "6. Exit"
    echo
}

function run_script() {
    local script_name="$1"
    echo "Starting the script: $script_name. Please wait..."
    sleep 5 
    bash "$script_name" | tee /dev/stdout &
    sleep 1
    printf "c\n" | tee /proc/$(jobs -p)/fd/0
    wait
}

banner
while true; do
    menu
    read -rp "Enter your choice (1-6): " choice
    case $choice in
        1) run_script "/PVUSD_Remote.sh" ;;
        2) run_script "/PVUSD_Assisted.sh" ;;
        3) echo "Placeholder 3. " ;;
        4) echo "Placeholder 4. " ;;
        5) echo "Placeholder 5. " ;;
        6) echo "Exiting. Goodbye!" && exit 0 ;;
        *) echo "Invalid option. Please try again." ;;
    esac
done
